function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('siteLang', lang);

    // Update HTML attributes
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';

    // Update Toggle Button Text
    const toggleBtn = document.getElementById('lang-toggle');
    if (toggleBtn) {
        toggleBtn.textContent = lang === 'ar' ? 'English' : 'عربي';
    }

    // Update all translatable text
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            el.innerHTML = translations[lang][key];
        }
    });

    // Update all translatable placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (translations[lang] && translations[lang][key]) {
            el.placeholder = translations[lang][key];
        }
    });

    // Update logos
    document.querySelectorAll('[data-i18n-logo]').forEach(el => {
        el.innerHTML = 'Abdelrahman <span>Amr</span>';
    });

    // Update active simulator if running
    if (typeof window.resetAndStartLawMindSimulator === 'function') {
        const activePage = document.querySelector('.page-view.active-page');
        if (activePage && activePage.id === 'page-portfolio') {
            window.resetAndStartLawMindSimulator();
        }
    }

    // Update active project details modal if open
    if (activeProjectId && typeof updateModalContent === 'function') {
        updateModalContent(activeProjectId, lang);
    }
}

